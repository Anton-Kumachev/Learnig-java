package lesson3;

public class Homework_lesson3_2 {

    public static void main(String[] args) {
        byte a = 75;
        byte b = 102;
        System.out.println("Первая переменная хранит значение: " + a);
        System.out.println("Вторая переменная хранит значение: " + b);
        System.out.println("Сумма переменных = " + (a + b));
        System.out.println("Разность переменных = " + (b - a));
        System.out.println("Деление переменных = " + ((double)b / (double)a));
        System.out.println("Произведение переменных = " + (a * b));
    }
}
